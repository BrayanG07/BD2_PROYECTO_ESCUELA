    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        if (txtUsuario.getText().length() == 0 || txtUsuario.getText().length() > 30) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un usuario", "Sistema Escolar", JOptionPane.INFORMATION_MESSAGE);
            txtUsuario.requestFocus();
            return;
        }
        if (txtClave.getText().length() == 0 || txtClave.getText().length() > 30) {
            JOptionPane.showMessageDialog(this, "Debes ingresar una contrase√±ia", "Sistema Escolar", JOptionPane.INFORMATION_MESSAGE);
            txtClave.requestFocus();
            return;
        }
        UsuariosControl control = new UsuariosControl();
        String respuesta = control.login(txtUsuario.getText(), txtClave.getText());
        if (respuesta.equals("1")) {
            this.dispose();
            frmPrincipal frm = new frmPrincipal();
            frm.toFront();
            frm.setVisible(true);
        }else {
            JOptionPane.showMessageDialog(this, "El Usuario no tiene acceso", "Sistema Escolar", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

